void DrawMap() {
	static int i = 0;
	int x, y;
	scr_clear();
		for (y = 0; y < HEIGHT; y++) {
			for (x = 0; x < WIDTH; x++) {
				if (map[y][x] != 0)
					gotoxy(x * 2, y);
				if (map[y][x] == 1) {
					if (y == 31) {
						textcolor(CYAN1, CYAN1);
						printscr("▣");
						textcolor(WHITE, CYAN1);
					}
					else {
						textcolor(RED1, BLACK);
						printscr("■");
						textcolor(WHITE, CYAN1);
					}
				}
				else if (map[y][x] == 2) {
					if (i % 2) {
						textcolor(YELLOW1, CYAN1);
						printscr("★");
						textcolor(WHITE, CYAN1);
					}
					else {
						textcolor(YELLOW1, CYAN1);
						printscr("☆");
						textcolor(WHITE, CYAN1);
					}
				}
				else if (map[y][x] == 4) {
					textcolor(GREEN2, GREEN2);
					printscr("■");
					textcolor(WHITE, CYAN1);
				}
				else if (map[y][x] == 5) {
					textcolor(GREEN1, GREEN1);
					printscr("■");
					textcolor(WHITE, CYAN1);
				}
				else if (map[y][x] == 99)
					printscr("@ ");
				else
				{
					textcolor(CYAN1, CYAN1);
					printscr("■");
					textcolor(WHITE, BLACK);

				}
			}
		}
		i = i ? 0 : 1;
}
